    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        try {
            Usuario cadastro = new Usuario();
            String nome = txtNome.getText();
            int idade = Integer.parseInt(txtIdade.getText());
            cadastro.setNome(nome);
            cadastro.setIdade(idade);
            UsuarioDAO.cadastrar(cadastro);
            JOptionPane.showMessageDialog(rootPane, "Cadastrado com sucesso", "Sucesso", HEIGHT);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Digite apenas n√∫meros no campo idade", 
                     "Erro", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnCadastrarActionPerformed

